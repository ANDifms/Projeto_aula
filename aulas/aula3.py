# laco repeticao

# 1- var de controle (inicializacao -> i)
# 2- condicao de parada (i < 10)
# 3- atualizacao da car de controle(incremento ou decremento)

# primeiro laco
# incremento

i = 0 #(1)
while i < 10: #(2)
    print(i)
    # os dois comandos fazem a mesma coisa, a diferenca é que a primeira forma é mais proficional 
    i +=1 #i = i+1
print("acabou") #(3)

# ------------------------------------------------------------------------------------------------
# decremento


i = 0 #(1)
while i > 0: #(2)
    print(i)
    # os dois comandos fazem a mesma coisa, a diferenca é que a primeira forma é mais proficional 
    i -=1 #i = i+1
print("acabou") #(3)


# ----------------------------------------------------
# par ou impar

i = 1 #(1)
while i <= 50: #(2)
    if i % 2 == 0:
        print(i)
    i += 1
print("cabo")

